squares = []
for value in range(1, 11):
    squares.append(value**2)

print(squares)

# The range() function in this code is generating a sequence of numbers from 1 to 10. 
# In Python, range(start, stop) generates a sequence of numbers starting from start, 
# up to but not including stop.

# So range(1, 11) generates the numbers 1, 2, 3, ..., 10. 
# These numbers are then used in the loop to calculate the square of each number.
